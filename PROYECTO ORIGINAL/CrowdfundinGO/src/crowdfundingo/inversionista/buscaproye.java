    public void mostrarDatos(String ID) throws SQLException, ClassNotFoundException {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(globalvars.conn, "crowdfundingo", "12345");
        con.setSchema("CROWDFUNDINGO");
        Statement s = con.createStatement();
        String query = "SELECT ID, TITULO, LEMA, DINERO, DESCRIPCION, LINK_IMG FROM PROYECTO WHERE ID=" + ID;
        System.out.println(query);
        s.executeQuery(query);
        ResultSet rs = s.getResultSet();
        while (rs.next()) {
            globalvars.idproy = rs.getString("ID");
            mp.setTitulovar(rs.getString("TITULO"));
            mp.setLemavar(rs.getString("LEMA"));
            mp.setDinerovar(rs.getString("DINERO"));
            mp.setDescripcionvar(rs.getString("DESCRIPCION"));
            mp.setLinkvar(globalvars.destiny + rs.getString("LINK_IMG"));
            idvar = rs.getString("ID");
        }
        System.out.println(rs);
        if (idvar.isEmpty() == false) {
            mp.show();
        } else {

        }
    }

