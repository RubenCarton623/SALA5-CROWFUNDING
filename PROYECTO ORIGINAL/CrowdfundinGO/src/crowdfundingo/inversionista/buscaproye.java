    public void llenarTablaProy(String titulo) throws SQLException, ClassNotFoundException {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(globalvars.conn, "crowdfundingo", "12345");
        con.setSchema("CROWDFUNDINGO");
        Statement s = con.createStatement();
        if (titulo.isEmpty() == false) {
            s.executeQuery("SELECT P.ID, TITULO,PAIS,'$'||DINERO AS DINERO_RECAUDADO,NOMBRES||' '||APELLIDOS AS EMPRENDEDOR FROM USUARIO U, PROYECTO P WHERE U.ID = P.ID_EMP AND P.TITULO='" + titulo + "'");
        } else {
            s.executeQuery("SELECT P.ID, TITULO,PAIS,'$'||DINERO AS DINERO_RECAUDADO,NOMBRES||' '||APELLIDOS AS EMPRENDEDOR FROM USUARIO U, PROYECTO P WHERE U.ID = P.ID_EMP");
        }
        ResultSet rs = s.getResultSet();
        System.out.println(rs);
        ProyTables.setModel(DbUtils.resultSetToTableModel(rs));
    }

